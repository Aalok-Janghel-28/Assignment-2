# Initialize the variable 'sum' to 0 to store the cumulative total.
sum = 0

# Loop through numbers from 1 to 50.
for i in range(1, 51):
    # Accumulate the current value of 'i' into 'sum'.
    sum = sum + i

# Print the result, which is the sum of numbers from 1 to 50.
print('The sum of the numbers from 1 to 50 is:', sum)